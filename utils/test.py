def fibonacci(n):
    if n <= 0:
        return []
    elif n == 1:
        return [0]
    elif n == 2:
        return [0, 1]
    else:
        fib = [0, 1]
        for i in range(2, n):
            fib.append(fib[i-1] + fib[i-2])
        for num in fib:
            print(num, end='\n')
        return fib


if __name__ == '__main__':
    print(fibonacci(50))